

function openPerfil(){
    document.getElementById('my-perfil').style.width = '15%';
    document.getElementById('my-perfil').style.border = 'solid  rgb(0, 28, 60) 4px';
}

function closePerfil(){
    document.getElementById('my-perfil').style.width = '0';
    document.getElementById('my-perfil').style.border = 'none';
}